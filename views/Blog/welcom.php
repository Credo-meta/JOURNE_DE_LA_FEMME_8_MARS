<h1>Cet article adopte un point de vue régional ou culturel particulier et nécessite une internationalisation</h1>
<p>L’histoire des femmes est une branche de l'histoire consacrée à l'étude des femmes en tant que groupe social, 
    apparue dans les années 1970 et étroitement liée aux luttes féministes1. 
    En effet, de nombreux auteurs constatent l'absence d'« historicité » des études sur les femmes2,3, 
    et analysent cette lacune comme un processus volontaire de déshistoricisation, 
    comme si la condition féminine ne dépendait que d'invariants réels et historiques4. 
    L'histoire des femmes devrait donc s'appuyer sur ce que le sociologue et philosophe français
     Pierre Bourdieu (1930-2002) appelle une « reconstitution de l'histoire du travail historique de déshistoricisation ».
    </p>


    <p>L'historiographie de l’histoire des femmes est, quant à elle, l’étude de la place de la femme dans l'histoire en tant que discipline — l'historiographie étant l'étude de la manière d'écrire l'histoire. Les femmes y sont étudiées dans leur rapport aux hommes, à la famille, à la société, etc., ainsi que l’évolution de ces rapports.

Dans la perspective féministe, jusqu'au milieu du xxe siècle, 
les sociétés occidentales accordent un traitement favorisant les hommes et assujettissant5 les femmes, 
tant au point de vue du droit que des usages et coutumes. En effet, les traditions accordent une importance particulière
 au rôle social de femme au foyer, qui doit se consacrer aux tâches ménagères, 
 à la reproduction et à l'éducation des enfants, même si ce rôle de femme au foyer n'exclut pas l'exercice
  d'une profession, notamment depuis la révolution industrielle au xixe siècle.
   La dévalorisation implicite de ce rôle de « femme traditionnelle » est récente, 
liée au poids grandissant pris par la sphère économique dans le monde</p>

<p>L'idée de l'égalité des sexes ne s'impose ainsi que récemment, 
mais les femmes ont su bien avant tenter de s'affranchir des contraintes et différences qui leur sont imposées. 
On ne saurait cependant parler d'une évolution continue de la condition féminine vers l'émancipation, 
car son histoire est ponctuée par d'importants retours en arrière, y compris lors des périodes révolutionnaires.
 Ainsi, la Réforme protestante (création du Protestantisme, qui débute au xvie siècle, 
 en Europe, et est aussi suivi d'une Réforme catholique), dans le domaine religieux, mais aussi 
 la Révolution française (à partir de 1789) et les socialismes, malgré leurs prétentions émancipatrices,
  tenteront souvent de les renvoyer au foyer.
 C'est ce décalage qui explique selon la sociologue française Andrée Michel (1920-2022) la naissance du féminisme</p>